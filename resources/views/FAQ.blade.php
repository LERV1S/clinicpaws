    <h1 class="text-4xl font-bold mb-4 py-4">Preguntas Frecuentes de ClinicPaws</h1>

    <h2 class="faqs">¿Cómo puedo agendar una cita?</h2>
    <p class="texto-grande">Puedes agendar una cita directamente en nuestra página web a través del módulo de citas o llamando al número (33) 1234-5678. También puedes visitarnos en la clínica para programar una cita en persona.</p>

    <h2 class="faqs">¿Cuáles son los horarios de atención?</h2>
    <p class="texto-grande">Nuestros horarios de atención son de lunes a viernes de 9:00 AM a 6:00 PM, y los sábados de 9:00 AM a 2:00 PM. Cerramos los domingos y días festivos.</p>

    <h2 class="faqs">¿Qué servicios ofrecen?</h2>
    <p class="texto-grande">Ofrecemos una variedad de servicios veterinarios, incluyendo consultas generales, vacunación, desparasitación, cirugías, hospitalización, laboratorio clínico y farmacia veterinaria.</p>

    <h2 class="faqs">¿Cómo puedo obtener los resultados de los análisis de mi mascota?</h2>
    <p class="texto-grande">Los resultados de los análisis se integran directamente en el expediente médico de tu mascota. Puedes acceder a ellos a través de nuestra plataforma en línea o solicitarlos en la clínica.</p>

    <h2 class="faqs">¿Aceptan emergencias?</h2>
    <p class="texto-grande">Sí, atendemos emergencias durante nuestros horarios de atención. Para emergencias fuera de horario, te recomendamos llamar previamente para confirmar disponibilidad o ser referido a un hospital de emergencias cercano.</p>

    <h2 class="faqs">¿Qué tipos de pago aceptan?</h2>
    <p class="texto-grande">Aceptamos pagos en efectivo, tarjetas de crédito y débito, así como transferencias electrónicas. También ofrecemos planes de financiamiento en ciertos procedimientos y servicios veterinarios.

    <h2 class="faqs">¿Ofrecen planes de bienestar o suscripciones?</h2>
    <p class="texto-grande">Sí, contamos con planes de bienestar personalizados que incluyen chequeos regulares, vacunación, desparasitación y otros servicios preventivos. Estos planes están diseñados para asegurar la salud continua de tu mascota a lo largo del año.</p>

    <h2 class="faqs">¿Puedo obtener una consulta veterinaria en línea?</h2>
    <p class="texto-grande">Sí, ofrecemos teleconsultas veterinarias para que puedas recibir asesoramiento sin necesidad de salir de casa. Es ideal para consultas de seguimiento o casos donde se requiera asesoramiento inicial antes de una visita presencial.</p>

    <h2 class="faqs">¿Qué debo hacer si mi mascota requiere cirugía?</h2>
    <p class="texto-grande">Si tu mascota necesita cirugía, te brindaremos toda la información necesaria sobre el procedimiento y cuidados pre y postoperatorios. Nuestro equipo te guiará en cada paso del proceso, asegurando que tu mascota reciba el mejor cuidado posible.</p>

    <h2 class="faqs">¿Cuentan con servicios de hospitalización?</h2>
    <p class="texto-grande">Sí, ofrecemos servicios de hospitalización para mascotas que requieren atención intensiva. Nuestro equipo está disponible las 24 horas para garantizar que tu mascota reciba la atención adecuada durante su recuperación.</p>

    <h2 class="faqs">¿Cómo puedo acceder a los resultados de laboratorio de mi mascota?</h2>
    <p class="texto-grande">Los resultados de laboratorio se integran directamente en el expediente médico de tu mascota. Puedes consultarlos en nuestra plataforma en línea o solicitarlos en la clínica.</p>

    <h2 class="faqs">¿Ofrecen asesoría nutricional para mascotas?</h2>
    <p class="texto-grande">Sí, ofrecemos servicios de asesoría nutricional para asegurar que tu mascota reciba una dieta adecuada según sus necesidades específicas. Nuestro equipo te orientará sobre la mejor alimentación según la edad, tamaño y condición de salud de tu mascota.</p>

    <h2 class="faqs">¿Es necesario agendar una cita para la vacunación o desparasitación?</h2>
    <p class="texto-grande">Recomendamos agendar una cita para la vacunación o desparasitación de tu mascota para garantizar una atención rápida y eficiente. Puedes hacerlo a través de nuestra página web o por teléfono.</p>

